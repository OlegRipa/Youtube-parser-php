<?php

namespace Youtube;

class Video implements VideoInterface
{
    /**
     * Video id
     * @var string
     */
    private $id;

    function __construct($id)
    {
        $this->id = $id;
    }

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param $id
     * @return $this
     */
    public function setId($id)
    {
        $this->id = $id;
        return $this;
    }
} 